    /// `runner_exit` keeps the child's own code in `child_code` even though `code`
    /// is what the process returns — the "child code is never lost" contract in
    /// its machine form.
    #[test]
    fn runner_exit_preserves_the_child_code_separately() {
        let child = Event::RunnerExit {
            code: 7,
            source: "child_exit",
            child_code: Some(7),
        };
        let value: serde_json::Value =
            serde_json::from_str(&serialize_record(&child, fixed_time()).unwrap()).unwrap();
        assert_eq!(value["code"], 7);
        assert_eq!(value["child_code"], 7);
        assert_eq!(value["source"], "child_exit");

        // A runner-imposed ending forwards a runner-band code and no child code.
        let timeout = Event::RunnerExit {
            code: 106,
            source: "timeout",
            child_code: None,
        };
        let value: serde_json::Value =
            serde_json::from_str(&serialize_record(&timeout, fixed_time()).unwrap()).unwrap();
        assert_eq!(value["code"], 106);
        assert!(value["child_code"].is_null());
    }

    /// The hand-rolled UTC formatter against well-known epoch instants.
    #[test]
    fn timestamp_matches_known_vectors() {
        let at = |secs: u64| format_rfc3339_utc(SystemTime::UNIX_EPOCH + Duration::from_secs(secs));
        assert_eq!(at(0), "1970-01-01T00:00:00.000Z");
        assert_eq!(at(86_400), "1970-01-02T00:00:00.000Z");
        // The classic Unix "billennium".
        assert_eq!(at(1_000_000_000), "2001-09-09T01:46:40.000Z");
        assert_eq!(at(1_609_459_200), "2021-01-01T00:00:00.000Z");
        // Millisecond precision is preserved.
        assert_eq!(
            format_rfc3339_utc(SystemTime::UNIX_EPOCH + Duration::from_millis(1_609_459_200_123)),
            "2021-01-01T00:00:00.123Z"
        );
    }
