    /// Whether `s` is a 64-character lowercase-hex string — the shape of an
    /// `argv_sha256` fingerprint.
    fn is_sha256_hex(s: Option<&str>) -> bool {
        matches!(s, Some(s) if s.len() == 64 && s.bytes().all(|b| matches!(b, b'0'..=b'9' | b'a'..=b'f')))
    }

    /// Redacted-by-default vs `--argv-raw`, and the redaction-safe fields filled
    /// either way. A bare run omits the raw argv but still carries the fingerprint
    /// (and a `hint` when the shape is recognized); `--argv-raw` adds the raw argv
    /// without changing the fingerprint or the hint.
    #[test]
    fn command_info_redacts_by_default_and_honors_argv_raw() {
        // A non-worker argv: redacted, fingerprinted, no recognized hint.
        let redacted = CommandInfo::for_argv(["cmd", "/c", "secret"], false);
        assert!(redacted.redacted && redacted.argv.is_none());
        assert!(
            is_sha256_hex(redacted.argv_sha256.as_deref()),
            "a redacted command still carries a hex fingerprint: {:?}",
            redacted.argv_sha256
        );
        assert!(
            redacted.hint.is_none(),
            "an unrecognized shape has no hint: {:?}",
            redacted.hint
        );

        // `--argv-raw` records the raw argv while keeping the same fingerprint/hint.
        let raw = CommandInfo::for_argv(["cmd", "/c", "secret"], true);
        assert!(!raw.redacted);
        assert_eq!(
            raw.argv.as_deref(),
            Some(["cmd", "/c", "secret"].map(String::from).as_slice())
        );
        assert_eq!(
            raw.argv_sha256, redacted.argv_sha256,
            "the fingerprint does not depend on whether the raw argv is disclosed"
        );
        assert_eq!(raw.hint, redacted.hint);
    }

    /// The hand-rolled SHA-256 against the FIPS 180-4 example vectors — the gate
    /// that lets the fixture (and the fingerprint) be trusted.
    #[test]
    fn sha256_matches_known_vectors() {
        assert_eq!(
            sha256_hex(b""),
            "e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855"
        );
        assert_eq!(
            sha256_hex(b"abc"),
            "ba7816bf8f01cfea414140de5dae2223b00361a396177a9cb410ff61f20015ad"
        );
        assert_eq!(
            sha256_hex(b"abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq"),
            "248d6a61d20638b8e5c026930c3e6039a33ce45964ff2167f6ecedd419db06c1"
        );
    }

    /// The argv fingerprint is SHA-256 over the NUL-joined argv: a single-element
    /// argv hashes exactly its one element (no separator), and the NUL boundary
    /// keeps element splits significant so distinct argvs cannot collide.
    #[test]
    fn argv_fingerprint_is_canonical_and_boundary_sensitive() {
        // One element: the join adds no separator, so it equals SHA-256("abc").
        assert_eq!(
            argv_sha256_hex(&["abc".to_string()]),
            "ba7816bf8f01cfea414140de5dae2223b00361a396177a9cb410ff61f20015ad"
        );
        // The NUL separator makes element boundaries significant: ["a","b"] and
        // ["ab"] are different argvs and must not fingerprint alike.
        assert_ne!(
            argv_sha256_hex(&["a".to_string(), "b".to_string()]),
            argv_sha256_hex(&["ab".to_string()])
        );
    }

    /// The hint classifier tags an MSBuild reusable-worker argv (case-insensitively,
    /// wherever the markers sit) and leaves everything else unclassified.
    #[test]
    fn classify_hint_recognizes_msbuild_and_ignores_unknown() {
        let msbuild = [
            "C:\\dotnet\\MSBuild.dll".to_string(),
            "/nodemode:1".to_string(),
            "/nodeReuse:true".to_string(),
        ];
        assert_eq!(classify_hint(&msbuild), Some("msbuild_node_reuse"));

        // Case-insensitive: a lowercased argv still matches.
        let lowercased = [
            "msbuild.dll".to_string(),
            "/nodemode:1".to_string(),
            "/nodereuse:true".to_string(),
        ];
        assert_eq!(classify_hint(&lowercased), Some("msbuild_node_reuse"));

        // Missing one marker → no match.
        let partial = ["MSBuild.dll".to_string(), "/nodemode:1".to_string()];
        assert_eq!(classify_hint(&partial), None);

        // An ordinary command is unclassified.
        assert_eq!(
            classify_hint(&["cmd".to_string(), "/c".to_string(), "build".to_string()]),
            None
        );
    }

